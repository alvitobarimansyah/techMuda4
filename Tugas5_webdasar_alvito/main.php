            <h1>Biografi Bill Gates</h1>
            Bernama lengkap William Henry Gates III atau lebih dikenal dengan nama Bill Gates dilahirkan pada 28 Oktober 1955, di 
            Seattle, Washington. Ia adalah adalah anak kedua dari tiga bersaudara. Ayahnya William Henry Gates adalah seorang pengacara 
            perusahaan yang punya banyak relasi di kota. Sedangkan ibunya Mary Maxwell seorang pegawai First Interstate Bank, Pacific 
            Northwest Bell dan anggota Tingkat Nasional United Way.

            <h1>Masa Kecil</h1>
            Bill Gates seorang anak yang cerdas, tetapi dia terlalu penuh semangat dan cenderung sering mengalami kesulitan di sekolah. 
            Ketika dia berumur sebelas tahun, orang tuanya memutuskan untuk membuat perubahan pada dirinya dan mengirimnya ke Lakeside 
            School, sebuah sekolah dasar yang bergengsi khusus bagi anak laki-laki.
                    
            <h1>Mengenal Komputer</h1>
            Di Lakeside itulah pada tahun 1968 Bill Gates untuk pertama kalinya diperkenalkan dengan dunia komputer, dalam bentuk mesin 
            teletype yang dihubungkan dengan telepon ke sebuah komputer pembagian waktu. Dia dengan cepat menguasai BASIC, sebuah bahasa 
            pemograman komputer, dan bersama dengan para hacker yang belajar sendiri di Lakeside. Dia melewatkan waktu ber-jam-jam 
            menulis program, melakukan permainan, dan secara umum mempelajari banyak hal tentang komputer. “Dia adalah seorang 
            eksentrik,” sebagaimana salah seorang guru memberikannya julukan itu. Bill Gates menempuh kuliah di Harvard University 
            di Cambridge mulai tahun 1975. Di sana ia bertemu dengan Paul Allen sewaktu sekolah bersama-sama. Bersama Paul Allen, 
            Bill Gates terus mengembangkan talentanya di bidang pemograman komputer. Namun, Bill gates memutuskan keluar (drop out) 
            untuk menyumbangkan wakunya ke Microsoft.
                    
            <h1>Mendirikan Microsoft</h1>
            Melalui usaha kerasnya, perusahaan yang ia dirikan yang bernama Microsoft Corporation menjadi sukses dan Bill Gates 
            melambung menjadi seorang jutawan. Di tahun 1990 Bill Gates sukses merilis sistem operasi yang sangat sukses di pasaran 
            dunia. Namun, Bill Gates juga mendapat reputasi yang tidak baik dalam karirnya. Tidak hanya satu kali, bahkan ia melakukan 
            beberapa kali kesalahan dalam bisnis perangkat lunaknya.
                    
            <h1>Monopoli Microsoft</h1>
            Dalam Biografi Bill Gates diketahui bahwa pada tahun 1990 Bill Gates mendapat tuntutan dari Departemen Keadilan Amerika 
            Serikat dengan dakwaan Gates telah melakukan monopoli terhadap perusahaan-perusahaan kecil. Dan sekali lagi Bill Gates 
            tersandung oleh hukum undang-undang bisnis Amerika Serikat pada tahun 1999. Pada tahun 2000, Bill gates mengundurkan diri 
            dari jabatannya sebagai Ketua Pegawai Eksekutif Bill Gates memilih kembali ke profesi lamanya yang ia cintai yaitu 
            Kepala Penelitian dan Pengembangan Perangkat Lunak di perusahaanya sendiri, Microsoft Corp. Dan pada awal tahun 2008, 
            Bill Gates memutuskan untuk mengundurkan diri dari manajemen Microsoft dan mengkonsentrasikan diri pada kerja kedermawanan 
            melalui yayasan sosial yang didirikannya, yaitu Bill dan Melinda Gates Foundation.
                    
            <h1>Keluarga Bill Gates</h1>
            Dalam kehidupan peribadinya, Gates menikah dengan Melinda French pada 1 Januari 1994. Mereka mempunyai tiga orang anak, 
            Jennifer Katherine Gates (1996), Rory John Gates (1999) dan Phoebe Adele gates (2002). Dalam Biografi Bill Gates diketahui 
            bahwa ia dengan istrinya, Bill Gates telah mendirikan Bill & Melinda Gates Foundation, yang merupakan sebuah Yayasan Sosial 
            yang memperhatikan pederita AIDS, beasiswa bagi universitas-universitas dan kepedulian pada dunia ketiga. Para kritikus 
            mengatakan ini merupakan pembuktian terhadap kemarahan banyak orang tentang praktik monopoli, adikuasa perusahaannya dan 
            beberapa kejahatan yang telah ia lakukan, tetapi beberapa orang yang dekat dengan Bill Gates berkata bahwa ia memang telah 
            lama berencana untuk menyumbangkan sebagian besar hartanya. Di tahun 1999 koran Washington Post memberitakan bahwa “Gates 
            telah menyatakan bahwa dia memutuskan untuk menyumbangkan $5 milyar kepada organisasi mereka. Hal itu tentu tidak 
            mengherankan karena menurut survei Majalah Forbes, Bill Gates selalu menjadi orang terkaya di dunia berturut-turut 
            selama tahun 1996 – 2004 dengan jumlah $ 90 Milyar. Dengan perjuangan dan tekad yang keras kini Bill Gates telah 
            menggapai cita-citanya. Untuk mencapai kesuksesan memang diperlukan pengorbanan dan perjuangan yang berat, namun usaha 
            keras dan sikap pantang menyerah Bill Gates telah membuahkan hasil yang dapat ia nikmati saat ini. Berturut turut dari 
            tahun ke tahun hingga saat ini Bill Gates bertengger diposisi satu sebagai orang terkaya di dunia.
                    
